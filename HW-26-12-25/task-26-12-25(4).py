#Напишіть програму, яка обчислює вартість поїздки на автомобілі.
#Користувач вводить відстань у кілометрах, витрату палива на 100
#кілометрів і ціну за літр бензину. Програма повинна вивести
#підсумкову вартість поїздки.

d = float(input("Дистанция: "))
a = float(input("Расход бензина на 100км: "))
petrol_price = float(input("1л бензина: "))

fuel = (d * a / 100)
price = (fuel * petrol_price)

print("стоимость поездки:", price)
